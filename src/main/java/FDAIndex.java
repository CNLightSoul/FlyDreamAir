    private void buyServiceButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buyServiceButtonActionPerformed
        if(editingTrip == null){
            System.out.println("Please select the trip again!");
            return;
        }
        String flightNo = editingTrip.getFlightNo();
        String departureDate = editingTrip.getDepartureDate();
        ArrayList<String> serviceList = editingTrip.checkServices();
        if(se01Box.isSelected()){
            if(!serviceList.contains("SE01")){
                currentCustomer.addService(flightNo,departureDate,"SE01");
                serviceModel.addElement("Snack pack");
            }
        }
        if(se02Box.isSelected()){
            if(!serviceList.contains("SE02")){
                currentCustomer.addService(flightNo,departureDate,"SE02");
                serviceModel.addElement("Sleeping gears");
            }
        }
        //reset the editingTrip
        editingTrip = currentCustomer.getSingleFutureTrip(flightNo, departureDate);
    }//GEN-LAST:event_buyServiceButtonActionPerformed

    private void removeServiceButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeServiceButtonActionPerformed
        if(editingTrip == null){
            System.out.println("Please select the trip again!");
            return;
        }
        String flightNo = editingTrip.getFlightNo();
        String departureDate = editingTrip.getDepartureDate();
        ArrayList<String> serviceList = editingTrip.checkServices();
        String removingServices = boughtServicesList.getSelectedValue();
        if(removingServices == null){
            return;
        }
        if(removingServices.equals("Snack pack")){
            System.out.println("removing service");
            serviceModel.removeElement("Snack pack");
            if(serviceList.contains("SE01")){
                currentCustomer.removeService(flightNo, departureDate, "SE01");
            }
            editingTrip = currentCustomer.getSingleFutureTrip(flightNo, departureDate);
            return;
        }
        if(removingServices.equals("Sleeping gears")){
            System.out.println("removing service");
            serviceModel.removeElement("Sleeping gears");
            if(serviceList.contains("SE02")){
                currentCustomer.removeService(flightNo, departureDate, "SE02");
            }
            editingTrip = currentCustomer.getSingleFutureTrip(flightNo, departureDate);
            return;
        }
    }//GEN-LAST:event_removeServiceButtonActionPerformed

