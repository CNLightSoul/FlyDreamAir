    private void searchFlightButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchFlightButtonActionPerformed
        //System.out.println("Button clicked");
        searchedFlights.clear();
        model.removeAllElements();
        foundFlightsList.clearSelection();
        bookFlightButton.setVisible(false);
        String departure = departureBox.getSelectedItem().toString();
        String destination = destinationBox.getSelectedItem().toString();
        Date departureDate = dateChooser.getDate();
        Date today = new Date();
        if(departureDate==null){
            warningLabel.setText("Please select a date!");
            return;
        }
        if(departure.equals(destination)){
            warningLabel.setText("Departure and destination can't be the same!");
            return;
        }
        if(departureDate.before(today)){
            warningLabel.setText("Please select a date later than today!");
        }
        String formattedDate = sdf.format(departureDate);
        for(Flight f: flightInfo){
            if(f.getDeparture().equals(departure) && f.getDestination().equals(destination)){
                String completedFlightInfo = f.getFlightNo() + " , From " + f.getFullDeparture() + " To " + f.getFullDestination() + " , " +formattedDate  + " , " + f.getTakeOff() + " , $" +f.getPrice();
                model.addElement(completedFlightInfo);
                searchedFlights.add(f);
            }
        }
        if(searchedFlights.isEmpty()){
            warningLabel.setText("No flights found!");
        }
        else{
            bookFlightButton.setVisible(true);
        }
    }//GEN-LAST:event_searchFlightButtonActionPerformed

