    private void changeSeatConfirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeSeatConfirmButtonActionPerformed
        if(editingTrip == null){
            System.out.println("Please select the trip again!");
            return;
        }
        String newSeatNumber = changeSeatRowBox.getSelectedItem().toString() + changeSeatNumberBox.getSelectedItem().toString();
        if(newSeatNumber.equals(editingTrip.getSeatNo())){
            System.out.println("Keep unchanged");
            changeSeatDialog.dispose();
            return;
        }
        //check the file
        String filePath = editingTrip.getFlightNo() + "/" + editingTrip.getDepartureDate() + ".txt";
        try {
            BufferedReader br = new BufferedReader(new FileReader(filePath));
            String line;
            while((line = br.readLine())!= null){
                if(line.contains(newSeatNumber)){
                    System.out.println("already occupied!");
                    return;
                }
            }
            currentCustomer.changeSeatNo(editingTrip.getFlightNo(), editingTrip.getDepartureDate(), newSeatNumber);
            changeSeatDialog.dispose();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FDAIndex.class.getName()).log(Level.SEVERE, null, ex);
            return;
        } catch (IOException ex) {
            Logger.getLogger(FDAIndex.class.getName()).log(Level.SEVERE, null, ex);
            return;
        }
        
    }//GEN-LAST:event_changeSeatConfirmButtonActionPerformed

