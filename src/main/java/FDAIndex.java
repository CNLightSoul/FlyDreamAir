    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        welcomeLabel.setText("Welcome, " + username + "!");
        try{
            currentCustomer = new Customer(username);
        }
        catch(Exception e){
            System.out.println("error happenes");
            dispose();
        }
        foundFlightsList.setModel(model);
        bookFlightButton.setVisible(false);
        //read the flights from txt
        BufferedReader br = null;
        try{
           br = new BufferedReader(new FileReader("flight.txt"));
           String line = null;
           while((line = br.readLine()) != null && !line.equals("")){
               String[] splittedLine = line.split(",");
               int flightPrice = Integer.parseInt(splittedLine[4]);
               Flight f = new Flight(splittedLine[0],splittedLine[1],splittedLine[2],splittedLine[3],flightPrice);
               flightInfo.add(f);
           }
        }
        catch(FileNotFoundException e){
            warningLabel.setText("Flight info file not found, please check the file!");
        }
        catch(IOException e){
            warningLabel.setText("Read file error!");
        }
        finally{
            try{
                if(br != null){
                    br.close();
                }
            }
            catch(IOException e){
                warningLabel.setText("read file error!");
            }
        }
    }//GEN-LAST:event_formWindowActivated

