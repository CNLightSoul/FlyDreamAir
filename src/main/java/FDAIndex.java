    private void seatConfirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seatConfirmButtonActionPerformed
        String seatNo = rowBox.getSelectedItem().toString() + numberBox.getSelectedItem().toString();
        System.out.println(seatNo);
        //currentCustomer.discardTempTrip();
        //check if this seat number is occupied;
        boolean seatOccupied = true;
        String filePath = currentCustomer.getTempTrip().getFlightNo() + "/" + currentCustomer.getTempTrip().getDepartureDate()+".txt";
        File dateFile = new File(filePath);
        if(dateFile.exists() && !dateFile.isDirectory()){
            try {
                seatOccupied = false;
                Scanner reader = new Scanner(dateFile);
                while(reader.hasNextLine()){
                    String line = reader.nextLine();
                    if(line.equals("")) continue;
                    String[] splittedLine = line.split(":");
                    if(splittedLine[0].equals(seatNo)){
                        seatOccupied = true;
                        break;
                    }
                }
            } catch (FileNotFoundException ex) {
                Logger.getLogger(FDAIndex.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            seatOccupied = false;
            try {
                dateFile.createNewFile();
            } catch (IOException ex) {
                Logger.getLogger(FDAIndex.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if(seatOccupied){
            
        }
        else{
            try {
                BufferedWriter bw = new BufferedWriter(new FileWriter(filePath,true));
                bw.append(seatNo + ":" + username);
                bw.close();
            } catch (IOException ex) {
                Logger.getLogger(FDAIndex.class.getName()).log(Level.SEVERE, null, ex);
            }
            currentCustomer.getTempTrip().setSeatNo(seatNo);
            currentCustomer.pushTempTrip();
            bookingSuccess.pack();
            bookingSuccess.setVisible(true);
            chooseSeatDialog.dispose();
        }
        
    }//GEN-LAST:event_seatConfirmButtonActionPerformed

